from collections import defaultdict, deque

def eulerian_path(adj_list):
    # Calculate in-degrees and out-degrees
    in_degree, out_degree = defaultdict(int), defaultdict(int)
    for node, neighbors in adj_list.items():
        out_degree[node] += len(neighbors)
        for neighbor in neighbors:
            in_degree[neighbor] += 1

    # Check for Eulerian path
    start_node, end_node = 0, 0
    for node, degree in out_degree.items():
        in_d = in_degree.get(node, 0)
        if abs(in_d - degree) > 1:
            return None
        elif degree - in_d == 1:
            start_node, end_node = node, end_node + 1
        elif in_d - degree == 1:
            start_node, end_node = end_node, node

    if start_node == end_node and start_node == 0:
        start_node = list(adj_list.keys())[0]

    # Hierholzer's algorithm to find Eulerian path
    stack, path = [start_node], deque()
    while stack:
        current = stack[-1]
        if current in adj_list and adj_list[current]:
            stack.append(adj_list[current].pop())
        else:
            path.appendleft(stack.pop())

    return "->".join(map(str, path))

